# Week 1 - Part 1: Addition and Subtraction
def main():
    print("Addition and Subtraction")
    num1 = float(input("Enter first number: "))
    num2 = float(input("Enter second number: "))
    total = num1 + num2
    diff = num1 - num2
    print(f"Sum: {total}")
    print(f"Difference: {diff}")

if __name__ == "__main__":
    main()
